// MapAppApp.swift
// Точка входа SwiftUI-приложения: объявляет главный объект приложения и корневую сцену.

import SwiftUI // Подключаем фреймворк SwiftUI для декларативного UI и жизненного цикла приложения

// @main помечает главный объект приложения.
// Структура MapAppApp соответствует протоколу App и описывает жизненный цикл приложения на SwiftUI.
@main
struct MapAppApp: App {
    // Храним состояние корневой view model на уровне приложения.
    // @State здесь гарантирует, что экземпляр будет жить, пока живёт сцена,
    // и при изменениях свойств vm SwiftUI сможет обновлять представления.
    // LocationsViewModel — источник данных и логика для экранов с локациями.
    @State private var vm = LocationsViewModel()
    
    // Описываем сцену (окно) приложения: что именно отображать при запуске.
    var body: some Scene {
        WindowGroup { // Группа окон (на iOS обычно одно окно) — корневая точка отображения UI
            LocationsView() // Корневой экран приложения с картой/списком локаций
                .environment(vm) // Передаём vm через окружение, чтобы дочерние представления могли получать её через @Environment(\._) без явной передачи по иерархии
        }
    }
}

